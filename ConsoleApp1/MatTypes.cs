namespace DefaultNamespace;

public class MatTypes
{
   
    public int Add(int a, int b)
    {
        return a + b;
    }
    
    
    public int Sub(int a, int b)
    {
        return a - b;
        
    }
    
    
    public int Divide(int a, int b)
    {
        return a/b;
    }
    
    
    
    public int Multication(int a, int b)
    {
        return a*b;
    }
    
    
}